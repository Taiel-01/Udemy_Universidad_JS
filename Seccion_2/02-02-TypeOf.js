//* Ejemplos de tipos de datos en JavaScript

//? Tipo de dato string
var nombre = "Mati";
console.log(typeof nombre);


//? Tipo de dato numerico
var numero = 10.5;
console.log(typeof numero);
var numero2 = 1000;
console.log(typeof numero2);


